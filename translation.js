let currentLanguage = localStorage.getItem('cl-language') || 'ar';

function t(key) {
    return (translations[currentLanguage] && translations[currentLanguage][key]) 
        || (translations['ar'] && translations['ar'][key]) 
        || key;
}

function applyLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('cl-language', lang);
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;

    // Update elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const val = t(key);
        if (val && val !== key) {
            if (el.tagName === 'INPUT' && el.placeholder !== undefined) {
                el.placeholder = val;
            } else {
                el.textContent = val;
            }
        }
    });

    // Update elements with data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });

    // Update lang buttons if they exist
    document.querySelectorAll('.settings-lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Dispatch event for page-specific listeners
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    applyLanguage(currentLanguage);
});
